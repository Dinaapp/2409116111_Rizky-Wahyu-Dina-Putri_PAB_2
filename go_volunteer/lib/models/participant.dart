class Participant {
  final String id;
  final String activityId;
  final String name;
  final String phone;
  final String email;
  final String institution;
  final String registeredAt;

  Participant({
    required this.id,
    required this.activityId,
    required this.name,
    required this.phone,
    required this.email,
    required this.institution,
    required this.registeredAt,
  });
}